const colorapple = document.getElementById("color-apple");

colorapple.addEventListener("click", (event) =>  {
  event.target.style.background = "linear-gradient(135deg,  #67d40eb9, #db2c0dc7)"; 
  event.target.style.width = "92%"; 
  event.target.style.margin = "auto";
  event.target.style.color = "black";
})

